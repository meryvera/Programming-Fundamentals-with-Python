# CONDICIONALES IF-ELIF-ELSE

# Declaración y Asignación de valores a las variables con entrada del usuario
var_numero = int(input("Ingresa un numero entero: "));

# Estructura condicional similar a switch-case
if var_numero == 1:
  print("Es lunes");
elif var_numero == 2:
  print("Es martes");
else:
  print("Es cualquier otro dia");