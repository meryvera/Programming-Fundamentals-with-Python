# BUCLES WHILE

# Declaración y Asignación de valor a la variable
var_contador = 1;

# Bucle while o mientras
while var_contador <=5:
  print("Este es el valor de la variable var_contador: ", var_contador);
  var_contador = var_contador + 2;

# Imprimir el valor final de la variable
print("El valor de la variable es: ", var_contador, " lo que es mayor a 5"); 